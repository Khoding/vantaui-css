/* ============================================================
   VantaUI — generate the dev-linter rule set.

   Reads the formalized conflicts in docs/compat-data.js (the "a ↔ b ↔ c"
   mutex declarations) and emits js/vantaui-lint-rules.js: the deduped list
   of helper-word groups where only one may sit on an element. The linter
   (js/vantaui-lint.js) imports it, so the rules ship inside the package and
   stay derived from the catalog (single source of truth) rather than being
   hand-maintained in two places.

   docs/compat-data.js is not published (it lives in docs/), so the rules are
   baked into js/ — which IS published — at build time.

     node scripts/gen-lint-rules.mjs

   Claude Generated, verified by hand.
   ============================================================ */
import {writeFile} from 'node:fs/promises';
import {resolve} from 'node:path';
import {pathToFileURL} from 'node:url';

const root = process.cwd();
const compatData = resolve(root, 'docs', 'compat-data.js');
const outFile = resolve(root, 'js', 'vantaui-lint-rules.js');

// Split a "a ↔ b ↔ c" conflict line into clean single-word members. Prose
// gotchas (no ↔, or members with spaces/parens) are skipped.
function mutexWords(conflict) {
  if (!conflict.includes('↔')) return null;
  const words = conflict
    .split('↔')
    .map(s => s.trim())
    .filter(w => /^[\w-]+$/.test(w));
  return words.length >= 2 ? words.sort() : null;
}

export async function genLintRules() {
  const {COMPONENTS} = await import(pathToFileURL(compatData).href);
  const seen = new Set();
  const groups = [];
  for (const c of COMPONENTS) {
    for (const conflict of c.conflicts || []) {
      const words = mutexWords(conflict);
      if (!words) continue;
      const key = words.join('|');
      if (seen.has(key)) continue;
      seen.add(key);
      groups.push(words);
    }
  }
  groups.sort((a, b) => a.join().localeCompare(b.join()));

  const body =
    '/* GENERATED from docs/compat-data.js by scripts/gen-lint-rules.mjs — do not edit by hand.\n' +
    '   Each array is a set of helper words that are mutually exclusive on one element\n' +
    '   (the catalog\'s "a ↔ b" conflicts). The dev linter flags any element carrying\n' +
    '   two or more words from the same set. Regenerated by `npm run build`. */\n' +
    'export const MUTEX_GROUPS = [\n' +
    groups.map(g => '  ' + JSON.stringify(g) + ',').join('\n') +
    '\n];\n';

  await writeFile(outFile, body);
  console.log(`✔ lint rules      ${groups.length} mutex group(s) → js/vantaui-lint-rules.js`);
}

// Run directly (node scripts/gen-lint-rules.mjs); also importable by the build.
if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  genLintRules().catch(e => {
    console.error(e);
    process.exit(1);
  });
}
