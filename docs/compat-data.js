/* ============================================================
   VantaUI — Compatibility catalog (canonical data)

   The single structured source of truth behind docs/compat.html. It is
   HAND-CURATED, exactly like the docs/llms-*.txt files: it does NOT
   regenerate from src/. When you add, rename, remove, or change a
   component, helper word, public --vui-* knob, or utility family, update
   the matching entry here IN THE SAME CHANGE (see CLAUDE.md → Source→docs
   map). Each entry is written from the prose header at the top of the
   corresponding src/ file — that header is the API source of truth.

   Schema
   ------
   COMPONENTS[] = {
     id        unique slug (also the kebab anchor used by compat.html)
     name      display name
     group     bucket, mirrors the llms topic split:
               Layout · Primitives · Forms · Data · Feedback · Navigation
     doc       llms topic file documenting it (deep-linked)
     live      gallery section id on index.html for a "view live" link
               (omit when the feature has no standalone demo)
     trigger   the real selector/element that activates it
     helpers[] { word, kind, note? }  element-scoped helper words.
               kind ∈ tone · size · shape · depth · layout · state · modifier
     knobs[]   { name, note }  PUBLIC custom properties only (skip --_ internals)
     combines  [componentId, …]  idiomatic nestings/pairings (matrix edges)
     conflicts [string, …]  mutually-exclusive words / known gotchas
     utilities [familyId, …]  utility families that meaningfully apply
   }

   UTILITIES[] = the applicability axis: each free-floating helper family
   and which component GROUPS it sensibly targets.
   ============================================================ */

